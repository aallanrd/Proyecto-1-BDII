package model;

/**
 *
 * @author Allan
 */
public class Proveedores {
    
    
    private String nombre;
    private String cedula;
    private String tipo;

    public Proveedores() {
    }

    public Proveedores(String nombre, String cedula, String tipo) {
        this.nombre = nombre;
        this.cedula = cedula;
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
